    private void txtNota4KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNota4KeyPressed
        if (evt.getKeyChar() == evt.VK_ENTER) {
            if (txtNota4.getText().isEmpty()) {
                txtNota4.grabFocus();
            } else {
                if (txtCantidadAlumnos.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Ingrese la cantidad de alumnos", "Mensaje del sistema", JOptionPane.ERROR_MESSAGE);
                    txtCantidadAlumnos.grabFocus();
                } else {
                    if (txtDataEstudiante.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Ingrese los nombres y apellidos del estudiante", "Mensaje del sistema", JOptionPane.ERROR_MESSAGE);
                        txtDataEstudiante.grabFocus();
                    } else {
                        Nota4 = Double.parseDouble(txtNota4.getText());

                        if (Nota4 < 0 || Nota4 > 20) {
                            JOptionPane.showMessageDialog(null, "Ingrese una nota 4 en el rango de [0-20]");
                        } else {
                            if (Item <= NumEstudiante) {
                                addTable();

                                if (Item.equals(NumEstudiante)) {
                                    BloquearTxt();
                                }
                            } else {
                                System.out.println("culmino");
                            }
                            
                            Item++;
                        }

                        
                    }
                }

            }
        }
    }//GEN-LAST:event_txtNota4KeyPressed

