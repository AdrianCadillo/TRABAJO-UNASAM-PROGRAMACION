   void addTable()
   {
      Promedio = (Nota1+Nota2+Nota3+Nota4)/4;
      
      if(Promedio>=13)
      {
          Condicion = "APROBADO";
          
          listaEstAprobados.addElement(Estudiante);
          
          listaPromAprobados.addElement(Promedio.toString());
      }else
      {
          Condicion = "DESAPROBADO";
          
           listaEstDesAprobados.addElement(Estudiante);
          
           listaPromDesAprobados.addElement(Promedio.toString());
      }
      
      Object  NotasEstudiante[] = {Estudiante,formato.format(Promedio),Condicion};
      
      modelEstudent.addRow(NotasEstudiante);
      
      LabelAprobados.setText(val.EstudentAprob_Desaprob(tablaEstudiante, 2,"APROBADO",NumEstudiante));
      
      LabelDesaprobados.setText(val.EstudentAprob_Desaprob(tablaEstudiante, 2,"DESAPROBADO",NumEstudiante));
      
      reset();
      
   }
